public class OrgInfo extends CountryCard {

    private String mName;
    private String mNameAbbr;
    private String mURL;

    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }

    public String getmNameAbbr() {
        return mNameAbbr;
    }

    public void setmNameAbbr(String mNameAbbr) {
        this.mNameAbbr = mNameAbbr;
    }

    public String getmURL() {
        return mURL;
    }

    public void setmURL(String mURL) {
        this.mURL = mURL;
    }

}
